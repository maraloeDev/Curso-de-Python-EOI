#Como mostrar el area y la circunferencia de un circulo
PI= 3.14
radius = float(input("Introduce el radio: "))
area = PI * radius * radius
circunferencia = 2.0 * PI * radius

print(f"El radio del circulo es {radius}")
print(f"El area del circulo es {area}")
print(f"La circunferencia del circulo es {circunferencia}")